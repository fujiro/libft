#include "libft.h"

int		ft_strcmp(char *s1, char *s2, size_t size)
{
	int		i;

	i = 0;
	while (s1[i] && s2[i] && i < size)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
