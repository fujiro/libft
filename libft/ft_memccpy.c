void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	char		*d;
	const char	*s;

	while (n > 0)
	{
		if (*s == c)
		{
			*d = *s;
			d++;
			s++;
			return (d);
		}
		s++;
		d++;
		n--;
	}
	return (NULL);
}
