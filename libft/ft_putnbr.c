#include "libft.h"

void	ft_putnbr(int n)
{
	int		i;

	i = 1;
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	while (n / i > 9)
		i = i * 10;
	while (i != 1)
	{
		ft_putchar(n / i + 48);
		n = n % i;
		i = i / 10;
	}
	ft_putchar(n % i + 48);
}
